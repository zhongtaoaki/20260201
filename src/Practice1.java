public class Practice1 {
    public static void main(String[] args) {

        int i = 10;

        // iが偶数なのか？奇数なのか？を判断したい
        if (i % 2 == 0) {
            // iが偶数の場合、偶数であることを宣言する
            System.out.println("偶数である");
        } else {
            // iが奇数の場合、奇数であることを宣言する
            System.out.println("奇数である");

        }
    }
}
